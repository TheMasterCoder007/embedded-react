bool er_host_start(const ErHostConfig* cfg, ErHost* host)
{
    memset(host, 0, sizeof(*host));

    SDL_SetMainReady();
    if (SDL_Init(SDL_INIT_VIDEO) != 0)
    {
        SDL_Log("SDL_Init failed: %s", SDL_GetError());
        return false;
    }

    host->window = SDL_CreateWindow(cfg->title ? cfg->title : "embedded-react",
                                    SDL_WINDOWPOS_CENTERED,
                                    SDL_WINDOWPOS_CENTERED,
                                    cfg->width,
                                    cfg->height,
                                    SDL_WINDOW_SHOWN | SDL_WINDOW_ALLOW_HIGHDPI);
    if (!host->window)
    {
        SDL_Log("SDL_CreateWindow failed: %s", SDL_GetError());
        SDL_Quit();
        return false;
    }

    host->renderer = SDL_CreateRenderer(host->window, -1, SDL_RENDERER_ACCELERATED | SDL_RENDERER_PRESENTVSYNC);
    if (!host->renderer)
    {
        SDL_Log("SDL_CreateRenderer failed: %s", SDL_GetError());
        SDL_DestroyWindow(host->window);
        SDL_Quit();
        return false;
    }

    host->phys_w = cfg->width;
    host->phys_h = cfg->height;
    SDL_GetRendererOutputSize(host->renderer, &host->phys_w, &host->phys_h);
    host->dpi_scale = (float)host->phys_w / (float)cfg->width;

    if (!er_sdl_backend_init(host->renderer, host->phys_w, host->phys_h))
    {
        SDL_Log("er_sdl_backend_init failed");
        SDL_DestroyRenderer(host->renderer);
        SDL_DestroyWindow(host->window);
        SDL_Quit();
        return false;
    }

    /* Hand the app's baked assets (images + fonts) to the engine now that the backend init above set
     * up the registries. Generated by `npm run build`; absent before the first build. */
#if defined(ER_HAVE_BAKED_ASSETS)
    er_register_assets();
#endif

    /* Boot QuickJS and publish the bridge + host globals. */
    host->rt = JS_NewRuntime();
    host_install_globals(host);

    host->running = true;
    host->prev_ticks = SDL_GetTicks();
    return true;
}

bool er_host_reload(ErHost* host, const char* explicit_path)
{
    /* Live reload (full remount): drop the JS context — freeing the whole React tree, the bridge's
     * GC-rooted registries, event handlers, and timers — reset the engine to an empty scene, then
     * bring up a fresh context and re-run the app. Component state is intentionally not preserved
     * (that's Fast Refresh, a later phase — see /SIMULATOR.md). Registered images/fonts survive the
     * reset, so baked assets keep resolving. */
    if (host->ctx)
    {
        JS_FreeContext(host->ctx);
        host->ctx = NULL;
    }
    er_reset();
    host_install_globals(host);
    return er_host_load_app(host, explicit_path);
}

void er_host_show_error(ErHost* host)
{
    /* RN-redbox style: clear the (possibly half-built) scene, publish the last captured error text as
     * a global, and run the built-in error app in the current context so the failure is on screen
     * instead of leaving a blank/stale window. The next successful reload replaces it. */
    er_reset();
    JSValue global = JS_GetGlobalObject(host->ctx);
    JS_SetPropertyStr(host->ctx, global, "__error", JS_NewString(host->ctx, s_last_error));
    JS_FreeValue(host->ctx, global);
    host_run(host->ctx, k_error_app, strlen(k_error_app), "<error-overlay>", false);
    er_bridge_pump(host->ctx);
}

bool er_host_load_app(ErHost* host, const char* explicit_path)
{
    /* Choose the app to run, in priority order:
     *   1. an explicit path (.qbc = bytecode, else source),
     *   2. the compiled bytecode bundle (app.bundle.qbc) next to the executable — the MCU load path,
     *   3. the source bundle (app.bundle.js) next to the executable,
     *   4. the built-in fallback JS app.
     */
    char* loaded = NULL;
    const char* app_src = k_default_app;
    size_t app_len = strlen(k_default_app);
    const char* app_name = "<builtin-app>";
    bool app_bytecode = false;

    if (explicit_path)
    {
        size_t len = 0;
        loaded = read_file(explicit_path, &len);
        if (loaded)
        {
            app_src = loaded;
            app_len = len;
            app_name = explicit_path;
            app_bytecode = is_bytecode_path(explicit_path);
        }
        else
        {
            SDL_Log("could not read '%s'; falling back to the bundled app", explicit_path);
        }
    }
    if (!loaded)
    {
        char* base = SDL_GetBasePath();
        if (base)
        {
            char path[1024];
            size_t len = 0;
            /* Prefer the compiled bytecode blob (no parser, faster boot) over the source bundle. */
            snprintf(path, sizeof(path), "%sapp.bundle.qbc", base);
            loaded = read_file(path, &len);
            if (loaded)
            {
                app_src = loaded;
                app_len = len;
                app_name = "app.bundle.qbc";
                app_bytecode = true;
            }
            else
            {
                snprintf(path, sizeof(path), "%sapp.bundle.js", base);
                loaded = read_file(path, &len);
                if (loaded)
                {
                    app_src = loaded;
                    app_len = len;
                    app_name = "app.bundle.js";
                }
            }
            SDL_free(base);
        }
    }

    /* Make the chosen path explicit — otherwise a silent fall-back from bytecode to source (e.g.
       the precompiler wasn't built, so app.bundle.qbc is missing) looks identical at runtime. */
    SDL_Log("running %s (%s)", app_name, app_bytecode ? "bytecode" : "source");

    const bool app_ok = host_run(host->ctx, app_src, app_len, app_name, app_bytecode);
    free(loaded);

    /* Settle any Promises / mount-time microtasks the app queued before the first paint. */
    if (app_ok)
    {
        er_bridge_pump(host->ctx);
    }
    return app_ok;
}

int er_host_event_px(const ErHost* host, int logical)
{
    return (int)((float)logical * host->dpi_scale + 0.5f);
}

bool er_host_step(ErHost* host)
{
    SDL_Event ev;
    while (SDL_PollEvent(&ev))
    {
        if (ev.type == SDL_QUIT)
        {
            host->running = false;
        }
        else if (ev.type == SDL_KEYDOWN && ev.key.keysym.sym == SDLK_ESCAPE)
        {
            host->running = false;
        }
        else if (ev.type == SDL_KEYDOWN && ev.key.keysym.sym == SDLK_r)
        {
            host->reload_requested = true; /* the simulator acts on this; the demo ignores it */
        }
        else if (ev.type == SDL_MOUSEBUTTONDOWN && ev.button.button == SDL_BUTTON_LEFT)
        {
            embedded_renderer_touch(
                0, ER_TOUCH_DOWN, er_host_event_px(host, ev.button.x), er_host_event_px(host, ev.button.y));
        }
        else if (ev.type == SDL_MOUSEBUTTONUP && ev.button.button == SDL_BUTTON_LEFT)
        {
            embedded_renderer_touch(
                0, ER_TOUCH_UP, er_host_event_px(host, ev.button.x), er_host_event_px(host, ev.button.y));
        }
        else if (ev.type == SDL_MOUSEMOTION && (ev.motion.state & SDL_BUTTON_LMASK))
        {
            embedded_renderer_touch(
                0, ER_TOUCH_MOVE, er_host_event_px(host, ev.motion.x), er_host_event_px(host, ev.motion.y));
        }
    }

    /* Service Promises and setTimeout/setInterval before painting, so timer/async-driven state
       updates land in this frame's commit. The engine clock used by timers was advanced by last
       frame's embedded_renderer_tick. */
    er_bridge_pump(host->ctx);

    er_commit();
    er_sdl_present();

    const uint32_t now = SDL_GetTicks();
    embedded_renderer_tick(now - host->prev_ticks);
    host->prev_ticks = now;

    return host->running;
}

void er_host_run(ErHost* host)
{
    host->prev_ticks = SDL_GetTicks();
    while (er_host_step(host))
    {
        /* step until quit */
    }
}

void er_host_shutdown(ErHost* host)
{
    if (host->ctx)
    {
        JS_FreeContext(host->ctx);
        host->ctx = NULL;
    }
    if (host->rt)
    {
        JS_FreeRuntime(host->rt);
        host->rt = NULL;
    }
    er_sdl_backend_destroy();
    if (host->renderer)
    {
        SDL_DestroyRenderer(host->renderer);
        host->renderer = NULL;
    }
    if (host->window)
    {
        SDL_DestroyWindow(host->window);
        host->window = NULL;
    }
    SDL_Quit();
}
