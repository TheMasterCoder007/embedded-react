    "NativeUI.commit();\n"
    "console.log('App built UI at', W + 'x' + H, '- tap a card');\n";

/**
 * @brief Last uncaught JS error (message + stack), captured by host_report for the error overlay.
 */
static char s_last_error[512] = "";

